    private Garagem criaObjeto(){
        Garagem g = new Garagem();
        if (txtCodigo.getText().isEmpty()){
            g.setId(0);
        }else{
            g.setId(Integer.parseInt(txtCodigo.getText()));
        }
        g.setNome_box(txtNome.getText());

        return(g);
    }
    
    private void populaComponentes(Garagem g){
        txtCodigo.setText(g.getId() + "");
        txtNome.setText(g.getNome_box());
    }
    
    private boolean camposValidos(){
        return((!txtNome.getText().trim().isEmpty()));
    }
    
    private void atualizaLista() throws SQLException{
        DefaultListModel lm = new DefaultListModel();
        lm.addAll(dao.read());
        listaGaragem.setModel(lm);
    }
